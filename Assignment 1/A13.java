/*
    the area of a right triangle is 184cm^2 and one of its legs is 16 cm long.Find the length of other leg
 */



class A13 {
    public static void main(String args[]){
        int AreaOfRightTriangle=184;
        int LengthOfOneLeg=16;
        int LengthOfOtherLeg=AreaOfRightTriangle*2/LengthOfOneLeg;
        System.out.println("Length Of Other Leg is:"+LengthOfOtherLeg);
    }
}
